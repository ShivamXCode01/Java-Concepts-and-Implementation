class Do_While_Loop{
    public static void main(String[] a){

        int N = 1 ;  // Initialisation

        do{
            System.out.println(N);
            N++;          // Increment
        } while(N <= 10);   // Condition check


    }
}

// The only difference between while and do while is that do while 
// give atleast one output even the condition is false