// Final Keyword :- it is used with variable ,method,class. we used it keyword where we want to not change the the value once 
// it is declare it behaves like a constant 

class L_20_Final_Keyword{
    public static void main(String a[]){

        final int age = 67; // it can't change further if we decalre it onces because it used with final keyword
        //age = 78;
        System.out.println(age);

    }
}

// if we make a method using final keyword then it stops method overriding